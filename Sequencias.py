def sequencias():
    # Sequência de números ímpares
    print(f"Próximo número da sequência a: {7 + 2}")

    # sequência de potências 
    print(f"Próximo número da sequência b: {64 * 2}")

    # quadrados perfeitos
    print(f"Próximo número da sequência c: {36 + 2 * 7 + 1}")

    # quadrados de números pares
    print(f"Próximo número da sequência d: {64 + 2 * 8 + 4}")

    # sequência de Fibonacci
    a, b = 5, 8
    print(f"Próximo número da sequência e: {a + b}")

    # sequência com incrementos variados
    print(f"Próximo número da sequência f: {19 + 1}")

# Chamar a função para responder
sequencias()
